package Models;

import java.sql.Date;

public class New {
    private int id;
    private String img;
    private Date date;
    private String description;

    public New() {
    }

    public New(int id, String img, Date date, String description) {
        this.id = id;
        this.img = img;
        this.date = date;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
     
    
}
